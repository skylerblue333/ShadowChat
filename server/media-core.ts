export class ContentDeliveryService {
  private readonly CDN_BASE = "https://cdn.skycoin4444.com";
  private readonly SIGNED_URL_TTL_SECONDS = 3600; // 1 hour

  generateSignedUrl(assetPath: string, userId: number, expiresInSeconds = this.SIGNED_URL_TTL_SECONDS): string {
    const expires = Math.floor(Date.now() / 1000) + expiresInSeconds;
    // In production: use AWS CloudFront signed URLs or similar
    // Signature = HMAC-SHA256(secret, assetPath + expires + userId)
    const signature = this.generateSignature(assetPath, expires, userId);
    return `${this.CDN_BASE}${assetPath}?expires=${expires}&user=${userId}&sig=${signature}`;
  }

  generatePublicUrl(assetPath: string): string {
    return `${this.CDN_BASE}${assetPath}`;
  }

  getOptimalCDNRegion(userRegion: string): string {
    const regionMap: Record<string, string> = {
      "us-east": "cdn-us-east.skycoin4444.com",
      "us-west": "cdn-us-west.skycoin4444.com",
      "eu-west": "cdn-eu-west.skycoin4444.com",
      "eu-central": "cdn-eu-central.skycoin4444.com",
      "ap-southeast": "cdn-ap-southeast.skycoin4444.com",
      "ap-northeast": "cdn-ap-northeast.skycoin4444.com",
    };
    return regionMap[userRegion] || this.CDN_BASE;
  }

  buildAdaptiveBitrateUrl(assetId: string, userBandwidthMbps: number): string {
    let quality: VideoQuality;
    if (userBandwidthMbps >= 25) quality = "1080p";
    else if (userBandwidthMbps >= 10) quality = "720p";
    else if (userBandwidthMbps >= 5) quality = "480p";
    else if (userBandwidthMbps >= 2) quality = "360p";
    else quality = "240p";

    return `${this.CDN_BASE}/media/videos/${assetId}/${quality}.m3u8`;
  }

  private generateSignature(path: string, expires: number, userId: number): string {
    // Simplified signature - in production use crypto.createHmac
    const data = `${path}${expires}${userId}`;
    let hash = 0;
    for (let i = 0; i < data.length; i++) {
      const char = data.charCodeAt(i);
      hash = ((hash << 5) - hash) + char;
      hash = hash & hash;
    }
    return Math.abs(hash).toString(36);
  }
}

