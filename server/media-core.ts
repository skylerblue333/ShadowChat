// STORAGE LIFECYCLE SERVICE
// ═══════════════════════════════════════════════════════════════

export class StorageLifecycleService {
  private readonly TIER_POLICIES = {
    hot: { maxAgeDays: 30, costPerGBMonth: 0.023 },
    warm: { maxAgeDays: 90, costPerGBMonth: 0.0125 },
    cold: { maxAgeDays: 365, costPerGBMonth: 0.004 },
    archive: { maxAgeDays: 2555, costPerGBMonth: 0.00099 },
    deleted: { maxAgeDays: 0, costPerGBMonth: 0 },
  };

  async evaluateAssetTier(asset: MediaAssetRecord): Promise<StorageTier> {
    const ageDays = (Date.now() - asset.uploadedAt.getTime()) / (1000 * 60 * 60 * 24);
    const recentViews = asset.viewCount;

    // High-traffic content stays hot
    if (recentViews > 1000) return "hot";
    if (recentViews > 100 && ageDays < 30) return "hot";
    if (recentViews > 10 && ageDays < 90) return "warm";
    if (ageDays < 365) return "cold";
    if (ageDays < 2555) return "archive";
    return "deleted";
  }

  async runLifecyclePolicies(assets: MediaAssetRecord[]): Promise<{
    migrated: number;
    deleted: number;
    costSavings: number;
  }> {
    let migrated = 0;
    let deleted = 0;
    let costSavings = 0;

    for (const asset of assets) {
      const recommendedTier = await this.evaluateAssetTier(asset);
      if (recommendedTier !== asset.storageTier) {
        const oldCost = this.TIER_POLICIES[asset.storageTier].costPerGBMonth;
        const newCost = this.TIER_POLICIES[recommendedTier].costPerGBMonth;
        const sizeGB = asset.fileSize / (1024 * 1024 * 1024);
        costSavings += (oldCost - newCost) * sizeGB;

        if (recommendedTier === "deleted") {
          deleted++;
        } else {
          migrated++;
        }
        asset.storageTier = recommendedTier;
      }
    }

    return { migrated, deleted, costSavings };
  }

  calculateStorageCost(assets: MediaAssetRecord[]): {
    totalCostPerMonth: number;
    breakdown: Record<StorageTier, { count: number; sizeGB: number; cost: number }>;
  } {
    const breakdown: Record<StorageTier, { count: number; sizeGB: number; cost: number }> = {
      hot: { count: 0, sizeGB: 0, cost: 0 },
      warm: { count: 0, sizeGB: 0, cost: 0 },
      cold: { count: 0, sizeGB: 0, cost: 0 },
      archive: { count: 0, sizeGB: 0, cost: 0 },
      deleted: { count: 0, sizeGB: 0, cost: 0 },
    };

    for (const asset of assets) {
      const tier = asset.storageTier;
      const sizeGB = asset.fileSize / (1024 * 1024 * 1024);
      breakdown[tier].count++;
      breakdown[tier].sizeGB += sizeGB;
      breakdown[tier].cost += sizeGB * this.TIER_POLICIES[tier].costPerGBMonth;
    }

    const totalCostPerMonth = Object.values(breakdown).reduce((sum, t) => sum + t.cost, 0);
    return { totalCostPerMonth, breakdown };
  }
}

